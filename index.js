

function clickVetor() {
    vetor.style.display = "none"
    imagem.style.display = "none"

    imagem_animada.style.display = "flex"
    vetor_close.style.display = "flex"
}
function clickVetorClose() {
    imagem_animada.style.display = "none"
    vetor_close.style.display = "none"

    vetor.style.display = "flex"
    imagem.style.display = "flex"
}
